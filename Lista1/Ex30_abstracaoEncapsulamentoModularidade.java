//Qual	a	diferença	entre	abstração,	encapsulamento	e	modularidade?

/*
 * A abstração consiste em reduzir o escopo da aplicação, focando apenas nas
 * partes relevantes e simplificando sua complexidade.
 * 
 * O encapsulamento refere-se ao processo de ocultar ou expor as partes de um
 * programa de maneira que apenas as entidades que realmente necessitam tenham
 * acesso a elas.
 * 
 * A modularidade envolve a divisão do programa em componentes menores, o que
 * facilita a legibilidade e melhora a manutenibilidade do código.
 */